// Animacija risanja poti
var currentStep = 0;

function animateSolution() {
    if (currentStep < solutionPath.length - 1) {
        ctx.beginPath();
        ctx.strokeStyle = "#ff0000";
        ctx.lineWidth = 2;
        ctx.lineCap = "square";
        ctx.moveTo(solutionPath[currentStep].x, solutionPath[currentStep].y);
        ctx.lineTo(solutionPath[currentStep + 1].x, solutionPath[currentStep + 1].y);
        ctx.stroke();
        currentStep++;
    } else {
        clearInterval(animationInterval);
    }
}

// Interval za animacijo
var animationInterval = setInterval(animateSolution, 100);